class Product:
    def __init__(self, type, name, price):
        self.type = type
        self.name = name
        self.price = price

class ProductStore:
    def __init__(self):
        self.products = []
        self.income = 0

    def add(self, product, amount):
        if not isinstance(product, Product):
            raise ValueError("Invalid product object")
        total_price = product.price * amount * 1.3  # Adding 30% price premium
        self.products.append((product, amount, total_price))

    def set_discount(self, identifier, percent, identifier_type='name'):
        for product, amount, total_price in self.products:
            if (identifier_type == 'name' and product.name == identifier) or \
               (identifier_type == 'type' and product.type == identifier):
                discounted_price = total_price * (1 - percent / 100)
                self.products.remove((product, amount, total_price))
                self.products.append((product, amount, discounted_price))

    def sell_product(self, product_name, amount):
        for product, current_amount, total_price in self.products:
            if product.name == product_name:
                if current_amount >= amount:
                    self.income += total_price * (amount / current_amount)
                    self.products.remove((product, current_amount, total_price))
                    self.products.append((product, current_amount - amount, total_price))
                else:
                    raise ValueError("Insufficient stock")
                return
        raise ValueError("Product not found")

    def get_income(self):
        return self.income

    def get_all_products(self):
        return [(product.name, amount) for product, amount, _ in self.products]

    def get_product_info(self, product_name):
        for product, amount, _ in self.products:
            if product.name == product_name:
                return (product.name, amount)
        return None

# Creating products
p = Product('Sport', 'Football T-Shirt', 100)
p2 = Product('Food', 'Ramen', 1.5)

# Creating a ProductStore instance
s = ProductStore()

# Adding products to the store
s.add(p, 10)
s.add(p2, 300)

# Selling products
s.sell_product('Ramen', 10)

# Getting product info
print(s.get_product_info('Ramen'))  # Output: ('Ramen', 290)

# Setting discount
s.set_discount('Ramen', 10, 'name')

# Getting all products
print(s.get_all_products())  # Output: [('Football T-Shirt', 10), ('Ramen', 290)]







































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































